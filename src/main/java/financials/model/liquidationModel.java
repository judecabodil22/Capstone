package financials.model;

public class liquidationModel {

	private String serial_no;
	private String date;
	private String responsibility_center;
	private String particulars;
	private String amount;
	private String total;
	
	public String getSerial_no() {
		return serial_no;
	}
	public void setSerial_no(String serial_no) {
		this.serial_no = serial_no;
	}
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	public String getResponsibility_center() {
		return responsibility_center;
	}
	public void setResponsibility_center(String responsibility_center) {
		this.responsibility_center = responsibility_center;
	}
	public String getParticulars() {
		return particulars;
	}
	public void setParticulars(String particulars) {
		this.particulars = particulars;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getTotal() {
		return total;
	}
	public void setTotal(String total) {
		this.total = total;
	}
	
	
	
	
	
	
}
