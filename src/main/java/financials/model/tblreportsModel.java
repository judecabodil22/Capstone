package financials.model;

public class tblreportsModel {

	private String dv_no;
	private String purpose_name;
	private String mode_of_payment;
	private String institute_name;
	private String employee_no;
	private String responsibility_center;
	private String amount;
	
	public String getDv_no() {
		return dv_no;
	}
	public void setDv_no(String dv_no) {
		this.dv_no = dv_no;
	}
	public String getPurpose_name() {
		return purpose_name;
	}
	public void setPurpose_name(String purpose_name) {
		this.purpose_name = purpose_name;
	}
	public String getMode_of_payment() {
		return mode_of_payment;
	}
	public void setMode_of_payment(String mode_of_payment) {
		this.mode_of_payment = mode_of_payment;
	}
	public String getInstitute_name() {
		return institute_name;
	}
	public void setInstitute_name(String institute_name) {
		this.institute_name = institute_name;
	}
	public String getEmployee_no() {
		return employee_no;
	}
	public void setEmployee_no(String employee_no) {
		this.employee_no = employee_no;
	}
	public String getResponsibility_center() {
		return responsibility_center;
	}
	public void setResponsibility_center(String responsibility_center) {
		this.responsibility_center = responsibility_center;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	
	
	
}
